//Camel convention(for classnames):All word's first letter must be in uppercase.
class FirstProgram 
{
	public static void main(String[] args) 
	{
		System.out.println("Hello World!");
		System.out.println("Hello !");
	}

	//pascal convention(for methods):first word's first letter should be in lowercase and
	//suceeding word's first letter should be in upper case.
	public static void secondMethodJava()
	{
		System.out.println("Hello World!");
	}
}
